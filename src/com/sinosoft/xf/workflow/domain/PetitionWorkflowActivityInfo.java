package com.sinosoft.xf.workflow.domain;

import javax.persistence.Entity;
import javax.persistence.Table;

import com.sinosoftframework.core.domain.IdEntity;
/**
 * 定义工作流状态以及业务信息
 * @date 2011-09-15
 * @author wangxx
 */
@Entity
@Table(name = "Petition_Workflow_Activity_Info")
public class PetitionWorkflowActivityInfo extends IdEntity {
	private static final long serialVersionUID = 1L;
	private String regionCode;
	private String regionName;
	private String activityNo;
	private String activityName;
	private String beforeAdvice;
	private String serviceClass;
	private String afterAdvice;
	/**
	 * @return the activityNo
	 */
	public String getActivityNo() {
		return activityNo;
	}
	/**
	 * @param activityNo the activityNo to set
	 */
	public void setActivityNo(String activityNo) {
		this.activityNo = activityNo;
	}
	/**
	 * @return the activityName
	 */
	public String getActivityName() {
		return activityName;
	}
	/**
	 * @param activityName the activityName to set
	 */
	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}
	/**
	 * @return the beforeAdvice
	 */
	public String getBeforeAdvice() {
		return beforeAdvice;
	}
	/**
	 * @param beforeAdvice the beforeAdvice to set
	 */
	public void setBeforeAdvice(String beforeAdvice) {
		this.beforeAdvice = beforeAdvice;
	}
	/**
	 * @return the serviceClass
	 */
	public String getServiceClass() {
		return serviceClass;
	}
	/**
	 * @param serviceClass the serviceClass to set
	 */
	public void setServiceClass(String serviceClass) {
		this.serviceClass = serviceClass;
	}
	/**
	 * @return the afterAdvice
	 */
	public String getAfterAdvice() {
		return afterAdvice;
	}
	/**
	 * @param afterAdvice the afterAdvice to set
	 */
	public void setAfterAdvice(String afterAdvice) {
		this.afterAdvice = afterAdvice;
	}
	public String getRegionCode() {
		return regionCode;
	}
	public void setRegionCode(String regionCode) {
		this.regionCode = regionCode;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
}
