package com.sinosoft.xf.petition.counter.domain;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Table;

import com.sinosoftframework.core.domain.IdEntity;
/**
 * 用户自定义对象信息
 * 2013-08-06
 * @author hxh
 */
@Entity
@Table(name = "PERSON_DEFINE_ITEM_INFO")//机构信息表
public class PersonDefineItemInfo extends IdEntity {
    private static final long serialVersionUID = 1L;
	private String xPosition;//横坐标
	private String yPosition;//列坐标
	private String operator;//操作人
	private Date operateDate;//操作时间
	private String code;
	private String name;
	public String getxPosition() {
		return xPosition;
	}
	public void setxPosition(String xPosition) {
		this.xPosition = xPosition;
	}
	public String getyPosition() {
		return yPosition;
	}
	public void setyPosition(String yPosition) {
		this.yPosition = yPosition;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public Date getOperateDate() {
		return operateDate;
	}
	public void setOperateDate(Date operateDate) {
		this.operateDate = operateDate;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
